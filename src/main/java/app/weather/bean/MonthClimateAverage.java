package app.weather.bean;

public class MonthClimateAverage {
	private String name;
	private String minAvgTempF;
	private String maxAbsAvgTempF;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getMinAvgTempF() {
		return minAvgTempF;
	}

	public void setMinAvgTempF(String minAvgTempF) {
		this.minAvgTempF = minAvgTempF;
	}

	public String getMaxAbsAvgTempF() {
		return maxAbsAvgTempF;
	}

	public void setMaxAbsAvgTempF(String maxAbsAvgTempF) {
		this.maxAbsAvgTempF = maxAbsAvgTempF;
	}

}
